/*Create variables to store the length and width of a room. 
Calculate and display the area. */
public class Q24 
{
    public static void main(String[] args) 
    {
        double length = 5.0; 
        double width = 4.0; 
        double area = length * width; 
        System.out.println("Length of the room: " + length + " meters");
        System.out.println("Width of the room: " + width + " meters");
        System.out.println("Area of the room: " + area + " square meters");   
    }
}
